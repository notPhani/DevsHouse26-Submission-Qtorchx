"""
vqd_excited_states.py
---------------------
Variational Quantum Deflation (VQD) to find the lowest 3 eigenvalues of the
H2/STO-3G Jordan-Wigner Hamiltonian using QtorchX's gate library.

Algorithm
---------
State 0: VQE  →  minimise  ⟨ψ₀|H|ψ₀⟩
State 1: VQD  →  minimise  ⟨ψ₁|H|ψ₁⟩ + β|⟨ψ₀|ψ₁⟩|²
State 2: VQD  →  minimise  ⟨ψ₂|H|ψ₂⟩ + β(|⟨ψ₀|ψ₂⟩|² + |⟨ψ₁|ψ₂⟩|²)

Run:
    cd <repo_root>
    python excited_states/vqd_excited_states.py

Outputs:
    excited_states/vqd_results.npz  — energies + loss history
"""

import sys, os, json
import numpy as np
import torch

